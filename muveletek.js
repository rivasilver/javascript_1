function osszead(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return a + b;
}
function kivon(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return a - b;
}
function szoroz(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return a * b;
}
function oszt(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return a / b;
}